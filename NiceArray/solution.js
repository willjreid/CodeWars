function isNice(arr){
  arr.sort(function(a,b) {return b - a});
  let hi = [];
  let lo = [];
  for (i = 0; i < arr.length - 1; i++) {
    hi.push(arr[i + 1] - arr[i]);
    lo.push(arr[i] - arr[i + 1]);
  };
  if (hi.contains = function(hi) {
    for (i = 0; i < hi.length; i++);
    i = 1;
  }) return true ;
  if (lo.contains = function(lo) {
    for (i = 0; i < lo.length; i++);
    i = 1;
  }) return true;
}

